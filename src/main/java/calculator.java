
public class calculator {

	public double add(double first, double secound){
		double addition;
		addition = first + secound;
		return addition;
	}
	
	public double sub(double first, double secound){
		double sub;
		sub = first - secound;
		return sub;
	}
	
	public double div(double first, double secound){
		double div;
		div = first / secound;
		return div;
	}
	public double multi(double first, double secound){
		double multi;
		multi = first * secound;
		return multi;
	
}
}